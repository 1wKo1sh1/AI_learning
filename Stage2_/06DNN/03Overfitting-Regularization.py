"""
=====================================================================================================
正则化（L1/L2）——参数约束

L2：让所有权重都趋近于 0 但不为 0，模型仍然使用所有神经元，但每个神经元的贡献被“平滑化”。

L1：让部分权重精确为 0，实现隐式的神经元/特征选择（相当于在参数层面自动“剪除”了无关特征）。
=======================================================================================================
为什么“不直接剪枝，而是惩罚权重”？

(1) 剪枝是离散的、后处理的操作
直接在训练中“去除神经元”是离散操作（要么保留，要么删除），难以通过梯度下降端到端优化。而正则化是连续可微的，可以在训练过程中平滑地约束模型。

(2) 模型可能“需要”所有神经元，只是不希望它们过大
深度学习中有个常见现象：许多神经元在训练后仍然有用，但如果不加正则化，它们的权重可能会变得非常大，导致过拟合。如果粗暴地剪掉它们，模型可能损失表达能力。L2 正则化允许这些神经元继续存在，但限制它们的“力量”，达到一种平衡。

(3) 正则化是一种软约束，剪枝是硬约束
软约束（正则化）：权重可以很小但不为零，保留了模型学习的灵活性。

硬约束（剪枝）：完全移除了某些神经元，模型容量被固定。

在数据量不太少的情况下，软约束往往比硬剪枝更能保留模型的泛化能力，因为模型仍然可以利用所有特征的微弱信号。

========================================================================================================
个人理解

“考虑的东西太多” 对应模型容量过大、权重参数过多或过大，导致模型过度学习训练数据中的噪声和细枝末节，失去了对“主干规律”的把握。

“有些属性又有细微的关联性，不能直接去除” 正是正则化优于简单剪枝的地方：很多特征虽然单独看贡献不大，但组合起来可能有微弱信号，直接删掉可能损失信息。

“对 w 加入惩罚机制，让网络用少量资源完成最高效的结果” 就是 L1/L2 正则化的本质——在保留所有潜在有用特征的前提下，迫使模型用尽量小的权重来拟合数据，相当于让模型学会“精打细算”，只动用必要的资源去抓住最重要的模式，而不是靠大权重强行记忆。

正则化与拉格朗日乘数法在数学形式上确实是相通的。正则化的优化问题可以看作是一个带约束优化问题的拉格朗日松弛形式
=========================================================================================================
如果是ax+bx2+cx3+dx4的这种网络，对于系数abcd实际上bcd是不需要的，那么正则化过程是如何意识到的

1. L1 正则化：主动将不必要系数变为 0
对于真实模型是线性的情况，训练数据中x2x3x4项与输出y实际上没有真实相关（假设无噪声或噪声很小）。因此，这些高阶项的“有效梯度”非常小（因为去掉它们后拟合误差几乎不变）。

在 L1 惩罚下，优化器会在“降低拟合误差”和“减少绝对值之和”之间权衡。由于增加高阶项只能极小地降低误差（甚至可能增加误差，如果噪声导致过拟合），但每增加一点系数都会付出λ的代价，所以最优解会让它们精确为 0。

从几何上看，L1 约束是一个菱形，最优解容易落在坐标轴上，即某些维度系数为 0。
结论：L1 正则化通过稀疏性自动选择特征，把不需要的高阶项系数“归零”，实现隐式特征选择。

2. L2 正则化：让不必要系数变小，但不为 0
L2 的惩罚是平方，梯度是2λw，当系数很小时，推力也变小，所以系数会趋近于 0，但通常不会精确为 0。

如果真实模型是线性的，高阶项系数会被压缩到非常小的值，对预测的贡献微乎其微。模型虽然保留了这些项，但它们的“有效容量”几乎为零。

所以 L2 不是“意识到”它们不需要，而是通过“削弱”它们来近似忽略它们。这样也避免了过拟合，但不会彻底删除它们。
=================================================================================================

"""
import numpy as np
import torch
import torch.nn as nn
import matplotlib.pyplot as plt

torch.manual_seed(4)

# 1.创造数据，数据集
points = np.array([[-0.5, 7.7], [1.2, 65.8], [0.4, 39.2], [-1.4, -15.7], [1.5, 75.6], [0.4, 34.0], [0.8, 62.3]])

# 分离特征与标签
x_train = points[:, 0]
y_train = points[:, 1]


# 2.定义前向模型
class Model(torch.nn.Module):
    def __init__(self):
        super(Model, self).__init__()
        self.layer1 = nn.Linear(1, 16)
        self.layer2 = nn.Linear(16, 32)
        self.layer3 = nn.Linear(32, 16)
        self.layer4 = nn.Linear(16, 1)

    def forward(self, x):
        x = torch.relu(self.layer1(x))
        x = torch.relu(self.layer2(x))
        x = torch.relu(self.layer3(x))
        x = self.layer4(x)
        return x


model = Model()
# 3.定义损失函数和优化器
lr = 0.05
cri = torch.nn.MSELoss()
# 集成起来
optimizer = torch.optim.Adam(model.parameters(), lr=lr, weight_decay=3)

# 4.开始迭代
epoches = 1000
for epoch in range(1, epoches + 1):
    x_train_tensor = torch.tensor(x_train, dtype=torch.float32)
    y_train_tensor = torch.tensor(y_train, dtype=torch.float32)
    # 前向传播
    y_pred = model(x_train_tensor.unsqueeze(1))
    # 计算损失
    loss = cri(y_pred.squeeze(1), y_train_tensor)

    # 反向传播与优化
    optimizer.zero_grad()
    loss.backward()
    optimizer.step()
    # 5.显示频率设置
    if epoch == 1 or epoch % 20 == 0:
        # 6.绘图
        plt.cla()
        plt.scatter(x_train, y_train)
        x_range = torch.tensor(np.linspace(-2, 2, 300), dtype=torch.float32).unsqueeze(1)
        y_range = model(x_range).detach().numpy()
        plt.plot(x_range, y_range)
        print(f"epoch: {epoch}, loss: {loss}")
        plt.pause(0.2)
plt.show()